/************************ 软件 I2C 底层时序（内部使用） ************************/
static void I2C_Start(void)
{
    P3OUT |= SDA_PIN;
    P3OUT |= SCL_PIN;
    __delay_cycles(10);
    P3OUT &= ~SDA_PIN;
    __delay_cycles(10);
    P3OUT &= ~SCL_PIN;
}

static void I2C_Stop(void)
{
    P3OUT &= ~SDA_PIN;
    P3OUT &= ~SCL_PIN;
    __delay_cycles(10);
    P3OUT |= SCL_PIN;
    __delay_cycles(10);
    P3OUT |= SDA_PIN;
}

static void I2C_Send_Byte(unsigned char data)
{
    unsigned char i;
    P3OUT &= ~SCL_PIN;
    for(i = 0; i < 8; i++)
    {
        if(data & 0x80)
            P3OUT |= SDA_PIN;
        else
            P3OUT &= ~SDA_PIN;
        data <<= 1;
        __delay_cycles(2);
        P3OUT |= SCL_PIN;
        __delay_cycles(5);
        P3OUT &= ~SCL_PIN;
        __delay_cycles(2);
    }
    // 释放应答位，简化不检测
    P3OUT |= SDA_PIN;
    __delay_cycles(5);
    P3OUT |= SCL_PIN;
    __delay_cycles(5);
    P3OUT &= ~SCL_PIN;
}

/************************ OLED 底层读写（内部使用） ************************/
static void OLED_Write_Cmd(unsigned char cmd)
{
    I2C_Start();
    I2C_Send_Byte(OLED_ADDR);
    I2C_Send_Byte(0x00);  // 命令流标识
    I2C_Send_Byte(cmd);
    I2C_Stop();
}

static void OLED_Write_Data(unsigned char data)
{
    I2C_Start();
    I2C_Send_Byte(OLED_ADDR);
    I2C_Send_Byte(0x40);  // 数据流标识
    I2C_Send_Byte(data);
    I2C_Stop();
}

/************************ 对外接口函数实现 ************************/
void OLED_Set_Pos(unsigned char x, unsigned char y)
{
    OLED_Write_Cmd(0xb0 + y);                  // 设置页地址 0~7
    OLED_Write_Cmd(((x & 0xf0) >> 4) | 0x10);  // 列地址高 4 位
    OLED_Write_Cmd(x & 0x0f);                  // 列地址低 4 位
}

void OLED_Clear(void)
{
    unsigned char y, x;
    for(y = 0; y < 8; y++)
    {
        OLED_Write_Cmd(0xb0 + y);
        OLED_Write_Cmd(0x00);
        OLED_Write_Cmd(0x10);
        for(x = 0; x < 128; x++)
            OLED_Write_Data(0x00);
    }
}

void OLED_Init(void)
{
    // 初始化 I2C 引脚为输出，默认高电平
    P3DIR |= SDA_PIN | SCL_PIN;
    P3OUT |= SDA_PIN | SCL_PIN;

    __delay_cycles(100000);  // 上电稳定延时
    OLED_Write_Cmd(0xAE);    // 关闭显示
    OLED_Write_Cmd(0xD5);    // 时钟分频与振荡频率
    OLED_Write_Cmd(0x80);
    OLED_Write_Cmd(0xA8);    // 多路复用率
    OLED_Write_Cmd(0x3F);    // 1/64 占空比
    OLED_Write_Cmd(0xD3);    // 显示偏移
    OLED_Write_Cmd(0x00);
    OLED_Write_Cmd(0x40);    // 显示起始行
    OLED_Write_Cmd(0x8D);    // 电荷泵
    OLED_Write_Cmd(0x14);    // 开启电荷泵
    OLED_Write_Cmd(0x20);    // 内存地址模式
    OLED_Write_Cmd(0x02);    // 页地址模式
    OLED_Write_Cmd(0xA1);    // 段重映射
    OLED_Write_Cmd(0xC8);    // COM 扫描方向
    OLED_Write_Cmd(0xDA);    // COM 引脚配置
    OLED_Write_Cmd(0x12);
    OLED_Write_Cmd(0x81);    // 对比度
    OLED_Write_Cmd(0xCF);
    OLED_Write_Cmd(0xD9);    // 预充电周期
    OLED_Write_Cmd(0xF1);
    OLED_Write_Cmd(0xDB);    // VCOMH 电压
    OLED_Write_Cmd(0x30);
    OLED_Write_Cmd(0xA4);    // 全局显示开启
    OLED_Write_Cmd(0xA6);    // 正常显示
    OLED_Write_Cmd(0xAF);    // 开启显示
    OLED_Clear();
}

void OLED_ShowChar(unsigned char x, unsigned char y, unsigned char chr)
{
    unsigned char c = chr - ' ';
    OLED_Set_Pos(x, y);
    OLED_Write_Data(F6x8[c][0]);
    OLED_Write_Data(F6x8[c][1]);
    OLED_Write_Data(F6x8[c][2]);
    OLED_Write_Data(F6x8[c][3]);
    OLED_Write_Data(F6x8[c][4]);
    OLED_Write_Data(F6x8[c][5]);
}

void OLED_ShowString(unsigned char x, unsigned char y, unsigned char *str)
{
    while (*str != '\0')
    {
        OLED_ShowChar(x, y, *str);
        x += 6;
        str++;
    }
}





